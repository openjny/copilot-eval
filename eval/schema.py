def _variant_schema() -> dict[str, Any]:
    return {
        "type": "object",
        "description": "An A/B environment: how its image is built and how its container "
        "is set up before Copilot runs.",
        "additionalProperties": False,
        "required": ["name"],
        "properties": {
            "name": _name_schema("Unique variant identifier, used as the image tag."),
            "description": _string(
                description="Human-readable summary of this variant.", default=""
            ),
            "build": {
                "type": "object",
                "description": "Image build settings.",
                "additionalProperties": False,
                "properties": {
                    "dockerfile": _string(
                        description="Path to a custom Dockerfile (FROM the copilot-eval base image). "
                        "Omit to use the base image unmodified."
                    ),
                },
            },
            "run": {
                "type": "object",
                "description": "Container setup.",
                "additionalProperties": False,
                "properties": {
                    "script": _string(
                        description="Script sourced inside the container before Copilot runs "
                        "(e.g. auth/login setup)."
                    ),
                },
            },
            "model": _string(description="Override runner.model for this variant only."),
            "vars": _vars_schema("Variant-level variable overrides (merged after task vars)."),
        },
    }


def _hooks_schema() -> dict[str, Any]:
    return {
        "type": "object",
        "description": "Setup/teardown scripts run around a task's Copilot invocation.",
        "additionalProperties": False,
        "properties": {
            "before_run": _string(
                description="Script run before Copilot (e.g. environment reset)."
            ),
            "after_run": _string(description="Script run after Copilot (e.g. cleanup)."),
            "on_failure": {
                "type": "string",
                "enum": ["fail", "warn"],
                "default": "fail",
                "description": "Failure policy for before_run: abort the run, or warn and continue. "
                "after_run failures always warn regardless of this setting.",
            },
        },
    }


def _evaluator_schema() -> dict[str, Any]:
    return {
        "type": "object",
        "description": "Evaluation criterion applied to a task's run.",
        "additionalProperties": False,
        "required": ["name"],
        "properties": {
            "name": _name_schema("Unique evaluator identifier (unique within its task)."),
            "type": {
                "type": "string",
                "enum": list(EVALUATOR_TYPES),
                "default": "judge",
                "description": "Evaluator strategy. Third-party plugins (issue #66) may register "
                "additional type strings not listed here.",
            },
            "prompt": _string(
                description="type=judge: the judge prompt (freeform). Mutually exclusive with "
                "'criterion'/'rubric'."
            ),
            "criterion": _string(
                description="type=judge: a scoring axis, paired with 'rubric' to compose the prompt."
            ),
            "rubric": {
                "type": "object",
                "description": "type=judge: maps an integer score (as a YAML int or numeric-string "
                "key) to an anchor description. Requires 'criterion'.",
                "additionalProperties": {"type": "string"},
            },
            "script": _string(
                description="type=script: path to an executable that scores the run."
            ),
            "value": {
                "description": "type=contains/regex: substring or regex pattern to match. "
                "type=metric: the numeric threshold to compare against (YAML key is still 'value').",
                "anyOf": [{"type": "string"}, {"type": "number"}],
            },
            "metric": {
                "type": "string",
                "enum": list(_metric_fields()),
                "description": "type=metric: RunMetrics field to assert on.",
            },
            "op": {
                "type": "string",
                "enum": list(METRIC_OPS),
                "description": "type=metric: comparison operator.",
            },
        },
        "allOf": [
            {
                # type omitted defaults to "judge" (see eval.config.Evaluator.type).
                "if": {
                    "anyOf": [
                        {"not": {"required": ["type"]}},
                        {"required": ["type"], "properties": {"type": {"const": "judge"}}},
                    ]
                },
                "then": {
                    "anyOf": [{"required": ["prompt"]}, {"required": ["criterion", "rubric"]}],
                    "not": {"required": ["prompt", "rubric"]},
                },
            },
            {
                "if": {"properties": {"type": {"const": "script"}}, "required": ["type"]},
                "then": {"required": ["script"]},
            },
            {
                "if": {
                    "properties": {"type": {"enum": ["contains", "regex"]}},
                    "required": ["type"],
                },
                "then": {"required": ["value"]},
            },
            {
                "if": {"properties": {"type": {"const": "metric"}}, "required": ["type"]},
                "then": {"required": ["metric", "op", "value"]},
            },
        ],
    }


def _task_schema() -> dict[str, Any]:
    return {
        "type": "object",
        "description": "A single eval scenario: a prompt run against every variant, scored by "
        "its evaluators.",
        "additionalProperties": False,
        "required": ["name", "prompt"],
        "properties": {
            "name": _name_schema("Unique task identifier, used in result file names."),
            "prompt": _string(
                description="Prompt sent to Copilot. Supports {var} interpolation from vars."
            ),
            "enabled": {
                "type": "boolean",
                "default": True,
                "description": "Set to false to skip this task without deleting it.",
            },
            "fixture": _string(
                description="Directory under fixtures/ to mount at /workspace. Falls back to the "
                "task name when unset."
            ),
            "fixtures": {
                "type": "array",
                "description": "Run this task against multiple fixtures (input-coverage axis) "
                "instead of a single one.",
                "items": _name_schema("Fixture directory name."),
                "uniqueItems": True,
            },
            "timeout_seconds": {
                "type": ["integer", "null"],
                "minimum": 1,
                "description": "Override runner.timeout_seconds for this task.",
            },
            "health_check": _string(
                description="Script that must pass before running this task's Copilot invocation."
            ),
            "vars": _vars_schema("Task-level variable overrides (merged after global vars)."),
            "hooks": _hooks_schema(),
            "evaluators": {
                "type": "array",
                "description": "Evaluation criteria applied to this task's runs.",
                "items": _evaluator_schema(),
            },
            # --- Backward-compatible aliases (see eval.config._parse_task) ---
            "judges": {
                "type": "array",
                "deprecated": True,
                "description": "Deprecated alias for evaluators (type=judge only). Prefer 'evaluators'.",
                "items": {
                    "type": "object",
                    "required": ["name", "prompt"],
                    "properties": {
                        "name": _name_schema("Judge name."),
                        "prompt": _string(description="Judge prompt."),
                    },
                },
            },
            "metrics": {
                "type": "object",
                "deprecated": True,
                "description": "Deprecated alias; use 'evaluators' instead.",
                "properties": {
                    "judges": {
                        "type": "array",
                        "items": {"type": "object"},
                    },
                },
            },
            "verify": _string(
                description="Deprecated alias for an evaluators entry of type=script named 'verify'.",
                deprecated=True,
            ),
            "reset_script": _string(
                description="Deprecated alias for hooks.before_run.", deprecated=True
            ),
        },
    }


def generate_schema() -> dict[str, Any]:
    """Build the full JSON Schema (2020-12) document for `eval-config.yaml`."""
    return {
        "$schema": "https://json-schema.org/draft/2020-12/schema",
        "$id": SCHEMA_ID,
        "title": "copilot-eval configuration",
        "description": "Schema for eval-config.yaml, the config file for copilot-eval's "
        "environment-isolated A/B evaluation runs. Generated from eval/config.py by "
        "scripts/generate_schema.py -- do not edit by hand.",
        "type": "object",
        "additionalProperties": False,
        "properties": {
            "vars": _vars_schema("Global variables available for {var} interpolation in prompts."),
            "runner": _runner_schema(),
            "variants": {
                "type": "array",
                "description": "A/B environments. Defaults to a single 'baseline' variant when "
                "omitted and no variants/*.yaml files exist.",
                "items": _variant_schema(),
            },
            "tasks": {
                "description": "Eval tasks, as a list (name required per item) or a mapping of "
                "task name to task body. Ignored when tasks/*.yaml files exist.",
                "anyOf": [
                    {"type": "array", "items": _task_schema()},
                    {
                        "type": "object",
                        "additionalProperties": {
                            **_task_schema(),
                            "required": ["prompt"],
                        },
                    },
                ],
            },
        },
    }
