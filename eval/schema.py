"""JSON Schema generation for `eval-config.yaml`.

Single source of truth for the schema shape: it pulls the enums, defaults, and
patterns straight from :mod:`eval.config` (and the small number of registries
it defers to, e.g. `eval.collectors.COLLECTOR_TYPES`) so the schema can never
drift from what `load_config` actually accepts.

The committed artifact (`schemas/eval-config.schema.json`) is generated by
`scripts/generate_schema.py`, which just calls :func:`generate_schema` and
writes the result. Re-run that script after changing any of the config
dataclasses / constants in `eval/config.py`.
"""

from __future__ import annotations

from typing import Any

from eval.config import (
    DEFAULT_COPILOT_VERSION,
    DEFAULT_OUTPUT_INSTRUCTION,
    EVALUATOR_TYPES,
    JUDGE_AGGREGATE_MODES,
    METRIC_OPS,
    OUTPUT_FORMATS,
    PARALLEL_MODES,
    VARIANT_ORDER_MODES,
)

# Same pattern as `eval.config._NAME_RE`, inlined here to avoid depending on a
# private symbol; keep in sync if that pattern ever changes.
_NAME_PATTERN = r"^[A-Za-z0-9][A-Za-z0-9._-]*$"
# Same patterns as `eval.config._CPUS_RE` / `_MEMORY_RE`, inlined for the same
# reason; keep in sync if those patterns ever change.
_CPUS_PATTERN = r"^\d+(\.\d+)?$"
_MEMORY_PATTERN = r"^\d+(\.\d+)?[bkmgBKMG]?$"

SCHEMA_ID = (
    "https://raw.githubusercontent.com/openjny/copilot-eval/main/schemas/eval-config.schema.json"
)


def _collector_types() -> tuple[str, ...]:
    """Local import: `eval.collectors` is a small, dependency-free registry,
    but importing it at module scope would add an import-order constraint for
    no benefit."""
    from eval.collectors import COLLECTOR_REGISTRY

    return tuple(sorted(COLLECTOR_REGISTRY))


def _runner_backends() -> tuple[str, ...]:
    """Local import, same rationale as `_collector_types` (see issue #66)."""
    from eval.runners import RUNNER_REGISTRY

    return tuple(sorted(RUNNER_REGISTRY))


def _metric_fields() -> tuple[str, ...]:
    from eval.trace import METRIC_FIELDS

    return METRIC_FIELDS


def _string(**kwargs: Any) -> dict[str, Any]:
    return {"type": "string", **kwargs}


def _vars_schema(description: str) -> dict[str, Any]:
    """`vars` maps are YAML scalars coerced to strings by the loader, so
    accept the common scalar types an author would naturally write."""
    return {
        "type": "object",
        "description": description,
        "additionalProperties": {"type": ["string", "number", "boolean", "null"]},
    }


def _name_schema(description: str) -> dict[str, Any]:
    return _string(description=description, pattern=_NAME_PATTERN, minLength=1)


# Same pattern as `eval.config._SHA256_RE`, inlined to avoid importing a private
# symbol; keep in sync if that pattern ever changes.
_SHA256_PATTERN = r"^[0-9a-f]{64}$"


def _remote_fixture_schema() -> dict[str, Any]:
    """A remote "dataset-as-code" fixture: `{url, sha256, name?}` (issue #122)."""
    return {
        "type": "object",
        "description": "Remote fixture fetched from 'url' and verified against 'sha256' "
        "(the digest of the downloaded archive/file), cached content-addressed for reuse.",
        "additionalProperties": False,
        "required": ["url", "sha256"],
        "properties": {
            "url": _string(description="URL of the fixture archive (.tar.gz/.zip) or plain file."),
            "sha256": _string(
                description="Expected sha256 of the downloaded bytes; a mismatch fails closed.",
                pattern=_SHA256_PATTERN,
            ),
            "name": _name_schema(
                "Fixture identity in the matrix/lockfile/manifest. Derived from the URL "
                "filename when omitted."
            ),
        },
    }


